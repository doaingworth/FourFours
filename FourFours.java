public class Four4s{
  public static void main(String[] args){
    System.out.println(44/44);
    System.out.println((4/4)+(4/4));
    System.out.println((4+4+4)/4);
    System.out.println(4*(4-4)+4);
    System.out.println(((4*4)+4)/4);
    System.out.println(((4 + 4) / 4) + 4);
    System.out.println(4 + 4 - (4 / 4));
    System.out.println(4 + 4 + 4 - 4);
    System.out.println(4 + 4 + (4 / 4));
    System.out.println((44 - 4) / 4);
  }
}
